# operações maatemáticas em expressões ou variáveis
produto_1 = 20
produto_2 = 10

print(produto_1 + produto_2)
print(produto_1 - produto_2)
print(produto_1 / produto_2)
print(produto_1 // produto_2)
print(produto_1 * produto_2)
print(produto_1 ** produto_2)
print(produto_1 % produto_2)

